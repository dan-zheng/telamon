use std::os::raw::c_int;

#[derive(Debug)]
pub enum OpenCLError {
    DEVICE_NOT_FOUND,
    DEVICE_NOT_AVAILABLE,
    COMPILER_NOT_AVAILABLE,
    MEM_OBJECT_ALLOCATION_FAILURE,
    OUT_OF_RESOURCES,
    OUT_OF_HOST_MEMORY,
    PROFILING_INFO_NOT_AVAILABLE,
    MEM_COPY_OVERLAP,
    IMAGE_FORMAT_MISMATCH,
    IMAGE_FORMAT_NOT_SUPPORTED,
    BUILD_PROGRAM_FAILURE,
    MAP_FAILURE,
    MISALIGNED_SUB_BUFFER_OFFSET,
    EXEC_STATUS_ERROR_FOR_EVENTS_IN_WAIT_LIST,
    COMPILE_PROGRAM_FAILURE,
    LINKER_NOT_AVAILABLE,
    LINK_PROGRAM_FAILURE,
    DEVICE_PARTITION_FAILED,
    KERNEL_ARG_INFO_NOT_AVAILABLE,
    INVALID_VALUE,
    INVALID_DEVICE_TYPE,
    INVALID_PLATFORM,
    INVALID_DEVICE,
    INVALID_CONTEXT,
    INVALID_QUEUE_PROPERTIES,
    INVALID_COMMAND_QUEUE,
    INVALID_HOST_PTR,
    INVALID_MEM_OBJECT,
    INVALID_IMAGE_FORMAT_DESCRIPTOR,
    INVALID_IMAGE_SIZE,
    INVALID_SAMPLER,
    INVALID_BINARY,
    INVALID_BUILD_OPTIONS,
    INVALID_PROGRAM,
    INVALID_PROGRAM_EXECUTABLE,
    INVALID_KERNEL_NAME,
    INVALID_KERNEL_DEFINITION,
    INVALID_KERNEL,
    INVALID_ARG_INDEX,
    INVALID_ARG_VALUE,
    INVALID_ARG_SIZE,
    INVALID_KERNEL_ARGS,
    INVALID_WORK_DIMENSION,
    INVALID_WORK_GROUP_SIZE,
    INVALID_WORK_ITEM_SIZE,
    INVALID_GLOBAL_OFFSET,
    INVALID_EVENT_WAIT_LIST,
    INVALID_EVENT,
    INVALID_OPERATION,
    INVALID_GL_OBJECT,
    INVALID_BUFFER_SIZE,
    INVALID_MIP_LEVEL,
    INVALID_GLOBAL_WORK_SIZE,
    INVALID_PROPERTY,
    INVALID_IMAGE_DESCRIPTOR,
    INVALID_COMPILER_OPTIONS,
    INVALID_LINKER_OPTIONS,
    INVALID_DEVICE_PARTITION_COUNT,
    INVALID_PIPE_SIZE,
    INVALID_DEVICE_QUEUE,
}

impl From<c_int> for OpenCLError {
    fn from(err: c_int) -> Self {
        use self::OpenCLError::*;
        match err {
            -1 => DEVICE_NOT_FOUND,
            -2 => DEVICE_NOT_AVAILABLE,
            -3 => COMPILER_NOT_AVAILABLE,
            -4 => MEM_OBJECT_ALLOCATION_FAILURE,
            -5 => OUT_OF_RESOURCES,
            -6 => OUT_OF_HOST_MEMORY,
            -7 => PROFILING_INFO_NOT_AVAILABLE,
            -8 => MEM_COPY_OVERLAP,
            -9 => IMAGE_FORMAT_MISMATCH,
            -10 => IMAGE_FORMAT_NOT_SUPPORTED,
            -11 => BUILD_PROGRAM_FAILURE,
            -12 => MAP_FAILURE,
            -13 => MISALIGNED_SUB_BUFFER_OFFSET,
            -14 => EXEC_STATUS_ERROR_FOR_EVENTS_IN_WAIT_LIST,
            -15 => COMPILE_PROGRAM_FAILURE,
            -16 => LINKER_NOT_AVAILABLE,
            -17 => LINK_PROGRAM_FAILURE,
            -18 => DEVICE_PARTITION_FAILED,
            -19 => KERNEL_ARG_INFO_NOT_AVAILABLE,
            -30 => INVALID_VALUE,
            -31 => INVALID_DEVICE_TYPE,
            -32 => INVALID_PLATFORM,
            -33 => INVALID_DEVICE,
            -34 => INVALID_CONTEXT,
            -35 => INVALID_QUEUE_PROPERTIES,
            -36 => INVALID_COMMAND_QUEUE,
            -37 => INVALID_HOST_PTR,
            -38 => INVALID_MEM_OBJECT,
            -39 => INVALID_IMAGE_FORMAT_DESCRIPTOR,
            -40 => INVALID_IMAGE_SIZE,
            -41 => INVALID_SAMPLER,
            -42 => INVALID_BINARY,
            -43 => INVALID_BUILD_OPTIONS,
            -44 => INVALID_PROGRAM,
            -45 => INVALID_PROGRAM_EXECUTABLE,
            -46 => INVALID_KERNEL_NAME,
            -47 => INVALID_KERNEL_DEFINITION,
            -48 => INVALID_KERNEL,
            -49 => INVALID_ARG_INDEX,
            -50 => INVALID_ARG_VALUE,
            -51 => INVALID_ARG_SIZE,
            -52 => INVALID_KERNEL_ARGS,
            -53 => INVALID_WORK_DIMENSION,
            -54 => INVALID_WORK_GROUP_SIZE,
            -55 => INVALID_WORK_ITEM_SIZE,
            -56 => INVALID_GLOBAL_OFFSET,
            -57 => INVALID_EVENT_WAIT_LIST,
            -58 => INVALID_EVENT,
            -59 => INVALID_OPERATION,
            -60 => INVALID_GL_OBJECT,
            -61 => INVALID_BUFFER_SIZE,
            -62 => INVALID_MIP_LEVEL,
            -63 => INVALID_GLOBAL_WORK_SIZE,
            -64 => INVALID_PROPERTY,
            -65 => INVALID_IMAGE_DESCRIPTOR,
            -66 => INVALID_COMPILER_OPTIONS,
            -67 => INVALID_LINKER_OPTIONS,
            -68 => INVALID_DEVICE_PARTITION_COUNT,
            -69 => INVALID_PIPE_SIZE,
            -70 => INVALID_DEVICE_QUEUE,
            _ => panic!("Invalid OpenCL error code"),
        }
    }
}
